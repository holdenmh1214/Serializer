public class Book {
    String title;
    String author;
    String genre;
    String publishDate;
    String protagonistName;

    public String getTitle() {
        return title;
    }

    public String getAuthor() {
        return author;
    }

    public String getGenre() {
        return genre;
    }

    public String getPublishDate() {
        return publishDate;
    }

    public String getProtagonistName() {
        return protagonistName;
    }

}
